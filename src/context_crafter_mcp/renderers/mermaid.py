GENERATED_HEADER = (
    f"<!-- GENERATED by context-crafter-mcp v{__version__}. "
    "Do not edit manually unless you intend to overwrite generated output. -->\n"
)


def _sanitize_mermaid_id(name: str) -> str:
    """Sanitize a string for use as a Mermaid node ID."""
    sanitized = "".join(c if c.isalnum() or c in "_-" else "_" for c in name)
    if sanitized and sanitized[0].isdigit():
        sanitized = "_" + sanitized
    if not sanitized:
        sanitized = "_"
    return sanitized[:60]


def _escape_mermaid_label(label: str) -> str:
    """Escape characters that break Mermaid node labels inside quotes."""
    return label.replace('"', "'").replace("[", "(").replace("]", ")")


def _is_test_or_temp(rel_path: str) -> bool:
    """Check if a file is likely a test or temp/debug script."""
    lower = rel_path.lower()
    parts = Path(lower).parts
    if any(p.startswith("test") for p in parts):
        return True
    if any(p in ("tests", "testing", "debug") for p in parts):
        return True
    name = Path(lower).name
    if name.startswith("debug_") or name.startswith("temp_") or name.startswith("tmp_"):
        return True
    return False


