def render_agent_brief(
    repo_path: str,
    detect: DetectResult,
    analysis: AnalysisResult,
    output_dir: str,
    generated_at: str | None = None,
) -> RenderResult:
    """Generate AGENT_BRIEF.md."""
    out = safe_output_path(Path(repo_path), output_dir)
    out.mkdir(parents=True, exist_ok=True)
    path = out / "AGENT_BRIEF.md"

    timestamp = generated_at or datetime.now(timezone.utc).isoformat()

    compact_eligible = _is_compact_eligible(analysis)

    # Ranked entry points with category tags
    ep_lines: list[str] = []
    for ep in analysis.likely_entry_points[: get_profile_limit(analysis.profile, "entry_points")]:
        cat = classify_path(ep)
        tag = f" [{cat.value}]" if cat not in (PathCategory.UNKNOWN, PathCategory.PRODUCT) else ""
        ep_lines.append(f"- {_safe_inline_code(ep)}{tag}")
    if not ep_lines:
        ep_lines = ["_None found_"]

    src_lines: list[str] = []
    for d in analysis.source_directories[: get_profile_limit(analysis.profile, "source_dirs")]:
        cat = classify_path(d)
        tag = f" [{cat.value}]" if cat != PathCategory.UNKNOWN else ""
        src_lines.append(f"- {_safe_inline_code(d)}{tag}")
    if not src_lines:
        src_lines = ["_None found_"]

    lines: list[str] = [
        GENERATED_HEADER,
    ]
    lines += _render_metadata_block(repo_path, analysis, timestamp)
    lines += [
        f"# Agent Brief: {Path(repo_path).resolve().name}",
        "",
        f"- **Types**: {', '.join(detect.project_types)}",
        "",
        "## What this is",
        "",
        "Compact, source-grounded context for AI coding agents.",
        "",
        "## Entry points",
        "",
    ]
    lines.extend(ep_lines)
    lines.append("")

    if analysis.source_directories or not compact_eligible:
        lines += [
            "## Source layout",
            "",
        ]
        lines.extend(src_lines)
        lines += [
            "",
        ]

    if analysis.test_directories or not compact_eligible:
        lines += [
            "## Test layout",
            "",
            _join(analysis.test_directories[: get_profile_limit(analysis.profile, "test_dirs")]),
            "",
        ]

    lines += [
        "## Key dependencies",
        "",
    ]
    deps: list[str] = []
    if analysis.python_dependencies:
        deps.extend(analysis.python_dependencies[: get_profile_limit(analysis.profile, "deps")])
    node_product_deps = []
    for pkg in analysis.node_packages:
        if pkg.category == "product" and pkg.dependencies:
            node_product_deps.extend(pkg.dependencies)
    if node_product_deps:
        deps.extend(sorted(set(node_product_deps))[: get_profile_limit(analysis.profile, "deps")])
    if not deps and analysis.rust_crates and analysis.rust_crates[0].dependencies:
        deps.extend(analysis.rust_crates[0].dependencies[: get_profile_limit(analysis.profile, "deps")])
    if not deps and analysis.go_modules:
        go_deps = []
        for mod in analysis.go_modules:
            go_deps.extend(mod.dependencies)
        if go_deps:
            deps.extend(sorted(set(go_deps))[: get_profile_limit(analysis.profile, "deps")])
    if not deps and analysis.java_projects:
        java_deps = []
        for java_proj in analysis.java_projects:
            java_deps.extend(java_proj.dependencies)
        if java_deps:
            deps.extend(sorted(set(java_deps))[: get_profile_limit(analysis.profile, "deps")])
    lines.append(_join(deps))
    lines.append("")
    lines += [
        "## Warnings",
        "",
    ]
    ss = analysis.scan_summary
    total_skipped = sum(ss.skipped_reasons.values())
    if analysis.errors:
        lines.append(f"- {len(analysis.errors)} analysis error(s); see full reports.")
    if ss.budget_exhausted or total_skipped > 100:
        lines.append(
            f"- Scan budget pressure: {total_skipped} file(s) skipped. "
            "Output is based on a bounded sample, not an exhaustive scan."
        )
    vendor_heavy = False
    if ss.category_counts:
        product_count = ss.category_counts.get("product", 0)
        unknown_count = ss.category_counts.get("unknown", 0)
        vendor_count = (
            ss.category_counts.get("vendor", 0)
            + ss.category_counts.get("generated", 0)
            + ss.category_counts.get("fixture", 0)
        )
        total_scanned = ss.files_scanned
        if total_scanned > 0 and (product_count + unknown_count) < total_scanned * 0.2:
            vendor_heavy = True
            lines.append(
                f"- Vendor/generated files dominate scan ({vendor_count} of {total_scanned}). "
                "Primary product surface may be smaller than it appears."
            )
    if not analysis.errors and not (ss.budget_exhausted or total_skipped > 100) and not vendor_heavy:
        lines.append("- None.")
    lines.append("")

    # Evidence/unknowns for agent brief
    unknowns = analysis.evidence_set.by_kind(EvidenceKind.UNKNOWN)
    unsupported = analysis.evidence_set.by_kind(EvidenceKind.UNSUPPORTED)
    if unknowns or unsupported:
        lines += [
            "## Unknowns / Limitations",
            "",
        ]
        for e in unknowns[:5]:
            lines.append(f"- {e.message}")
