def _extract_architecture_notes(analysis: AnalysisResult) -> dict[str, list[str]]:
    """Extract architecture patterns and notes from the analysis."""
    notes: dict[str, list[str]] = {
        "patterns": [],
        "abstractions": [],
        "module_relationships": [],
        "concerns": [],
    }

    # Python-specific architecture analysis
    if analysis.python_modules:
        source_modules = [m for m in analysis.python_modules if not _is_test_or_temp(m.rel_path)]

        # Detect plugin/registry pattern
        registry_modules = [
            m for m in source_modules if "registry" in m.rel_path.lower() or "register" in " ".join(m.functions)
        ]
        if registry_modules:
            notes["patterns"].append("Plugin/Registry pattern detected (dynamic analyzer registration).")

        # Detect pipeline/orchestration pattern
        pipeline_modules = [
            m
            for m in source_modules
            if any(p in m.rel_path.lower() for p in ["graph", "pipeline", "workflow", "orchestr"])
        ]
        if pipeline_modules:
            notes["patterns"].append("Pipeline/Orchestration pattern detected (state-machine or graph-based flow).")

        # Detect server/API pattern
        server_modules = [m for m in source_modules if "server" in m.rel_path.lower()]
        if server_modules:
            notes["patterns"].append("Server/API pattern detected.")

        # Count key abstractions
        all_classes: list[str] = []
        all_async_funcs: list[str] = []
        for m in source_modules:
            for c in m.classes:
                all_classes.append(f"{m.rel_path}::{c}")
            for f in m.async_functions:
                all_async_funcs.append(f"{m.rel_path}::{f}")

        if all_classes:
            notes["abstractions"].append(f"{len(all_classes)} class(es) defined across source modules.")
            for c in all_classes[: get_profile_limit(analysis.profile, "abstractions")]:
                notes["abstractions"].append(f"  - {_safe_inline_code(c)}")
            if len(all_classes) > 10:
                notes["abstractions"].append(f"  - ... and {len(all_classes) - 10} more")

        if all_async_funcs:
            notes["abstractions"].append(
                f"{len(all_async_funcs)} async function(s) — likely I/O-bound or event-driven architecture."
            )

        # Module relationships
        internal_edges: set[tuple[str, str]] = set()
        for m in source_modules:
            raw_src = m.module_name or Path(m.rel_path).parts[0] if Path(m.rel_path).parts else ""
            # Strip src/ prefixes
            for prefix in ("src.", "lib.", "source.", "sources."):
                if raw_src.startswith(prefix):
                    raw_src = raw_src[len(prefix) :]
            src_pkg = raw_src.split(".")[0] if raw_src else ""
            for imp in m.internal_imports:
                first = imp.split(".")[0]
                if first and first != src_pkg:
                    internal_edges.add((src_pkg, first))

        if internal_edges:
            notes["module_relationships"].append("Cross-module dependencies detected:")
            for src, tgt in sorted(internal_edges)[: get_profile_limit(analysis.profile, "internal_edges")]:
                notes["module_relationships"].append(f"  - {_safe_inline_code(src)} → {_safe_inline_code(tgt)}")
            if len(internal_edges) > get_profile_limit(analysis.profile, "internal_edges"):
                notes["module_relationships"].append(f"  - ... and {len(internal_edges) - 15} more")

        # Cyclic dependency check (simple)
        cycles: list[str] = []
        for src, tgt in internal_edges:
            if (tgt, src) in internal_edges:
                cycle = f"{_safe_inline_code(src)} ↔ {_safe_inline_code(tgt)}"
                if cycle not in cycles:
                    cycles.append(cycle)
        if cycles:
            notes["concerns"].append("Potential bidirectional/cyclic dependencies detected:")
            for c in cycles[: get_profile_limit(analysis.profile, "cycles")]:
                notes["concerns"].append(f"  - {c}")

        # Large modules
        large_modules = [m for m in source_modules if len(m.classes) > 10 or len(m.functions) > 20]
        if large_modules:
            notes["concerns"].append("Large modules (high class/function count) — consider decomposition:")
            for m in large_modules[: get_profile_limit(analysis.profile, "large_modules")]:
                notes["concerns"].append(
                    f"  - {_safe_inline_code(m.rel_path)} ({len(m.classes)} classes, {len(m.functions)} functions)"
                )

    # Go-specific patterns
    if analysis.go_modules:
        all_interfaces = []
        all_entry_points = []
        for mod in analysis.go_modules:
            all_interfaces.extend(mod.interfaces)
            all_entry_points.extend(mod.entry_points)
        if len(all_interfaces) > 3:
            notes["patterns"].append("Interface-heavy design (Go idiomatic polymorphism).")
        if all_entry_points:
            notes["patterns"].append("CLI/service architecture detected (main package entry points).")

    # Java-specific patterns
    if analysis.java_projects:
        all_annotations = []
        total_classes = 0
        for java_proj in analysis.java_projects:
            all_annotations.extend(java_proj.annotations)
            total_classes += len(java_proj.classes)
        spring_annotations = {"Component", "Service", "Controller", "Repository", "Entity", "SpringBootApplication"}
        found_spring = spring_annotations & set(all_annotations)
        if found_spring:
            notes["patterns"].append(f"Spring framework detected ({', '.join(sorted(found_spring))}).")
        if total_classes > 20:
            notes["abstractions"].append(f"{total_classes} class(es) — class-heavy architecture.")

    # Node-specific patterns (check product packages first)
    if analysis.node_packages:
        web_frameworks = {"express", "fastify", "koa", "hapi", "nest"}
        frontend_frameworks = {"react", "vue", "angular", "svelte", "next"}
        for node_pkg in analysis.node_packages:
            if node_pkg.category == "vendor":
                continue
            all_deps = set(node_pkg.dependencies) | set(node_pkg.dev_dependencies)
            found_web = web_frameworks & all_deps
            found_fe = frontend_frameworks & all_deps
            if found_web:
                notes["patterns"].append(f"Web server framework detected ({', '.join(sorted(found_web))}).")
            if found_fe:
                notes["patterns"].append(f"Frontend framework detected ({', '.join(sorted(found_fe))}).")

    # Rust-specific patterns
    if analysis.rust_crates:
        crate = analysis.rust_crates[0]
        if crate.traits and len(crate.traits) > 2:
            notes["patterns"].append("Trait-based polymorphism detected.")
        if any("tokio" in d for d in crate.dependencies):
            notes["patterns"].append("Async runtime detected (tokio).")

    return notes


def render_architecture_summary(
    repo_path: str,
    detect: DetectResult,
    analysis: AnalysisResult,
    output_dir: str,
    generated_at: str | None = None,
) -> RenderResult:
    """Generate ARCHITECTURE_SUMMARY.md."""
    out = safe_output_path(Path(repo_path), output_dir)
    out.mkdir(parents=True, exist_ok=True)
    path = out / "ARCHITECTURE_SUMMARY.md"

    timestamp = generated_at or datetime.now(timezone.utc).isoformat()

    arch_notes = _extract_architecture_notes(analysis)
    has_patterns = any(arch_notes.values())
    compact_eligible = _is_compact_eligible(analysis)

    headings: list[str] = [
        "Detected Stacks",
        "Major Source Directories",
        "Likely Runtime Entry Points",
        "Testing Layout",
        "Packaging / Build System",
        "Documentation State",
    ]
    if arch_notes["patterns"] or not compact_eligible:
        headings.append("Architecture Patterns")
    if arch_notes["abstractions"] or not compact_eligible:
        headings.append("Key Abstractions")
    if arch_notes["module_relationships"] or not compact_eligible:
        headings.append("Module Relationships")
    if analysis.workspace_packages or len(analysis.node_packages) > 1:
        headings.insert(3, "Workspace / Monorepo Layout")
    headings.append("Risks / Unknowns")

    if analysis.errors:
        headings.insert(0, "Errors")

    lines: list[str] = [
        GENERATED_HEADER,
    ]
    lines += _render_metadata_block(repo_path, analysis, timestamp)
    lines += [
        f"# Architecture Summary: {Path(repo_path).resolve().name}",
        "",
    ]

    lines += _toc_for_headings(headings)
    lines += _maybe_errors_section(analysis)

    lines += [
        "## Detected Stacks",
        "",
        _join(detect.project_types),
        "",
        "## Major Source Directories",
        "",
    ]
    for d in analysis.source_directories[: get_profile_limit(analysis.profile, "source_dirs")]:
        cat = classify_path(d)
        tag = f" [{cat.value}]" if cat != PathCategory.UNKNOWN else ""
        lines.append(f"- {_safe_inline_code(d)}{tag}")
    lines += ["", "## Likely Runtime Entry Points", ""]
    for ep in analysis.likely_entry_points[: get_profile_limit(analysis.profile, "entry_points")]:
        cat = classify_path(ep)
        tag = f" [{cat.value}]" if cat not in (PathCategory.UNKNOWN, PathCategory.PRODUCT) else ""
        lines.append(f"- {_safe_inline_code(ep)}{tag}")
    lines += [
        "",
        "## Testing Layout",
        "",
        _join(analysis.test_directories[: get_profile_limit(analysis.profile, "test_dirs")]),
        "",
        "## Packaging / Build System",
        "",
    ]

    if "python" in detect.project_types:
        lines.append("- Python project detected (look for `pyproject.toml`, `setup.py`)")
    if "node" in detect.project_types:
        lines.append("- Node project detected (look for `package.json`, build scripts)")
    if "dotnet" in detect.project_types:
        lines.append("- .NET project detected (look for `.sln`, `.csproj`)")
    if "rust" in detect.project_types:
        lines.append("- Rust project detected (look for `Cargo.toml`)")
    if "go" in detect.project_types:
        lines.append("- Go project detected (look for `go.mod`)")
    if "java" in detect.project_types:
        java_markers = detect.markers.get("java", [])
        has_pom = any("pom.xml" in m for m in java_markers)
        has_gradle = any("build.gradle" in m for m in java_markers)
        if has_pom and has_gradle:
            lines.append("- Java project detected (look for `pom.xml`, `build.gradle`)")
        elif has_pom:
            lines.append("- Java project detected (look for `pom.xml`)")
        elif has_gradle:
            lines.append("- Java project detected (look for `build.gradle`)")
        else:
            lines.append("- Java project detected")
    lines.append("")

    if analysis.workspace_packages or len(analysis.node_packages) > 1:
        lines += [
            "## Workspace / Monorepo Layout",
            "",
        ]
        if analysis.workspace_packages:
            lines.append(f"- Workspace patterns: {', '.join(analysis.workspace_packages)}")
        for pkg in analysis.node_packages[:5]:
            pkg_cat = pkg.category or "unknown"
            lines.append(
                f"- {_safe_inline_code(pkg.name or pkg.rel_path or 'unknown')} [{pkg_cat}]: {len(pkg.files)} file(s), {len(pkg.dependencies)} runtime dep(s)"
            )
        if len(analysis.node_packages) > 5:
            lines.append(f"- ... and {len(analysis.node_packages) - 5} more package(s)")
        lines.append("")

    lines += [
        "## Documentation State",
        "",
        f"- README: {'Yes' if any('readme' in f.lower() for f in analysis.root_files) else 'Not obvious'}",
        f"- Docs files: {len(analysis.docs_files)}",
        "",
    ]

    if arch_notes["patterns"] or not compact_eligible:
        lines += [
            "## Architecture Patterns",
            "",
        ]
        if arch_notes["patterns"]:
            for note in arch_notes["patterns"]:
                lines.append(f"- {note}")
        else:
            lines.append("_No specific architectural patterns identified._")
        lines.append("")

    if arch_notes["abstractions"] or not compact_eligible:
        lines += [
            "## Key Abstractions",
            "",
        ]
        if arch_notes["abstractions"]:
            for note in arch_notes["abstractions"]:
                lines.append(f"- {note}")
        else:
            lines.append("_No significant abstractions identified._")
        lines.append("")

    if arch_notes["module_relationships"] or not compact_eligible:
        lines += [
            "## Module Relationships",
            "",
        ]
        if arch_notes["module_relationships"]:
            for note in arch_notes["module_relationships"]:
                lines.append(note)
        else:
            lines.append("_No cross-module relationships identified._")
        lines.append("")

    lines += [
        "## Risks / Unknowns",
        "",
    ]

    if arch_notes["concerns"]:
        for note in arch_notes["concerns"]:
            lines.append(f"- {note}")

    if not analysis.likely_entry_points:
        lines.append("- No clear entry points were identified automatically.")
    if not analysis.test_directories:
        lines.append("- No test directories were identified automatically.")
    if not analysis.config_files:
        lines.append("- No obvious config files found.")
    if len(analysis.root_files) < 3:
        lines.append("- Repository root appears sparse; may be a monorepo subdirectory or incomplete checkout.")
    ss = analysis.scan_summary
    total_skipped = sum(ss.skipped_reasons.values())
    if ss.budget_exhausted or total_skipped > 100:
        lines.append(
            f"- Scanner budget exhausted or heavily constrained: {total_skipped} file(s) skipped. "
            "Conclusions are based on a bounded sample."
        )
    if ss.category_counts:
        product_count = ss.category_counts.get("product", 0)
        unknown_count = ss.category_counts.get("unknown", 0)
        vendor_count = (
            ss.category_counts.get("vendor", 0)
            + ss.category_counts.get("generated", 0)
            + ss.category_counts.get("fixture", 0)
        )
        total_scanned = ss.files_scanned
        if total_scanned > 0 and (product_count + unknown_count) < total_scanned * 0.2:
            lines.append(
                f"- Scan composition is mostly vendor/generated/tooling ({vendor_count} of {total_scanned} files). "
                "Primary product surface may be smaller than file count suggests."
            )
    if analysis.errors:
        lines.append("- Some files could not be parsed; see analysis errors.")
    if not has_patterns and not any(
        [
            "python" in detect.project_types,
            "node" in detect.project_types,
            "dotnet" in detect.project_types,
            "rust" in detect.project_types,
            "go" in detect.project_types,
            "java" in detect.project_types,
        ]
    ):
        lines.append("- No mainstream stack markers found; architecture inference is limited.")
    if not arch_notes["concerns"] and analysis.likely_entry_points and analysis.test_directories:
        lines.append("- No obvious architectural concerns detected from static analysis.")

    lines += [
        "",
        "---",
        "*This analysis is based purely on static inspection. It may miss runtime architecture or cloud infra.*",
        "",
    ]

    write_redacted_text(path, "\n".join(lines))
    return RenderResult(
        ok=True,
        written=[str(path)],
        files_scanned=analysis.scan_summary.files_scanned,
        project_types=detect.project_types,
        resolved_output_dir=str(out),
        scan_summary=analysis.scan_summary,
    )


