GENERATED_HEADER = (
    f"<!-- GENERATED by context-crafter-mcp v{__version__}. "
    "Do not edit manually unless you intend to overwrite generated output. -->\n"
)


def _join(items: list[str]) -> str:
    if not items:
        return "_None found_"
    return "\n".join(f"- {i}" for i in items)


def _safe_inline_code(text: str) -> str:
    """Wrap text in inline code, escaping backticks by using double backticks."""
    if not text:
        return "``"
    if "`" in text:
        return f"``{text}``"
    return f"`{text}`"


@dataclass(frozen=True)
class CommandHint:
    """A statically inferred command with evidence and confidence."""

    command: str
    purpose: str
    confidence: str
    evidence: str


def _render_metadata_block(repo_path: str, analysis: AnalysisResult, generated_at: str) -> list[str]:
    """Render a consistent metadata block for markdown files."""
    lines: list[str] = [
        f"- **Generated by**: context-crafter-mcp v{__version__}",
        f"- **Generated at**: {generated_at}",
        f"- **Source repo**: {Path(repo_path).resolve().name}",
    ]
    ss = analysis.scan_summary
    if ss.git_commit:
        lines.append(f"- **Git commit**: {ss.git_commit}")
    lines.append(f"- **Profile**: {analysis.profile}")
    lines.append(
        f"- **Scanner**: max_files={ss.max_files}, max_depth={ss.max_depth}, max_file_bytes={ss.max_file_bytes}"
    )
    lines.append("")
    return lines


def _has_file(analysis: AnalysisResult, name: str) -> bool:
    """Return true when a scanned file/root file has the given basename."""
    target = name.lower()
    candidates = analysis.root_files + analysis.config_files
    return any(Path(candidate).name.lower() == target for candidate in candidates)


def _append_command(hints: list[CommandHint], seen: set[str], hint: CommandHint) -> None:
    if hint.command in seen:
        return
    seen.add(hint.command)
    hints.append(hint)


def _collect_command_hints(analysis: AnalysisResult) -> list[CommandHint]:
    """Collect likely project commands from static markers and parsed package metadata."""
    hints: list[CommandHint] = []
    seen: set[str] = set()

    if _has_file(analysis, "pyproject.toml"):
        _append_command(
            hints,
            seen,
            CommandHint(
                "uv sync --extra dev",
                "Install Python project and development dependencies",
                "medium",
                "pyproject.toml",
            ),
        )
        if analysis.test_directories:
            _append_command(
                hints,
                seen,
                CommandHint(
                    "uv run python -m pytest -q",
                    "Run Python tests",
                    "medium",
                    "pyproject.toml and test directory",
                ),
            )
    elif _has_file(analysis, "requirements.txt"):
        _append_command(
            hints,
            seen,
            CommandHint(
                "python -m pip install -r requirements.txt",
                "Install Python dependencies",
                "medium",
                "requirements.txt",
            ),
        )
        if analysis.test_directories:
            _append_command(
                hints,
                seen,
                CommandHint("python -m pytest -q", "Run Python tests", "low", "requirements.txt and test directory"),
            )

    for pkg in analysis.node_packages:
        if pkg.dependencies or pkg.dev_dependencies or _has_file(analysis, "package-lock.json"):
            _append_command(
                hints,
                seen,
                CommandHint("npm install", "Install Node dependencies", "medium", pkg.rel_path or "package.json"),
            )
        for script in ("test", "lint", "typecheck", "build", "start", "dev"):
            if script in pkg.scripts:
                _append_command(
                    hints,
                    seen,
                    CommandHint(
                        f"npm run {script}",
                        f"Run Node `{script}` script",
                        "high",
                        f"{pkg.rel_path or 'package.json'} scripts.{script}",
                    ),
                )

    if analysis.go_modules:
        _append_command(hints, seen, CommandHint("go test ./...", "Run Go tests", "medium", "go.mod"))
        _append_command(hints, seen, CommandHint("go build ./...", "Build Go packages", "medium", "go.mod"))

    if analysis.rust_crates:
        _append_command(hints, seen, CommandHint("cargo test", "Run Rust tests", "medium", "Cargo.toml"))
        _append_command(hints, seen, CommandHint("cargo build", "Build Rust crate/workspace", "medium", "Cargo.toml"))

    for java_project in analysis.java_projects:
        if java_project.build_tool == "maven" or _has_file(analysis, "pom.xml"):
            _append_command(hints, seen, CommandHint("mvn test", "Run Java tests", "medium", "pom.xml"))
            _append_command(hints, seen, CommandHint("mvn package", "Build Java package", "medium", "pom.xml"))
        if (
            java_project.build_tool == "gradle"
            or _has_file(analysis, "build.gradle")
            or _has_file(analysis, "build.gradle.kts")
        ):
            gradle = "gradlew.bat" if _has_file(analysis, "gradlew.bat") else "gradle"
            _append_command(hints, seen, CommandHint(f"{gradle} test", "Run Java tests", "medium", "Gradle build file"))
            _append_command(
                hints, seen, CommandHint(f"{gradle} build", "Build Java project", "medium", "Gradle build file")
            )

    if analysis.dotnet_solutions or analysis.dotnet_projects:
        _append_command(
            hints, seen, CommandHint("dotnet restore", "Restore .NET dependencies", "medium", "solution/project file")
        )
        _append_command(hints, seen, CommandHint("dotnet test", "Run .NET tests", "low", "solution/project file"))
        _append_command(
            hints, seen, CommandHint("dotnet build", "Build .NET solution/project", "medium", "solution/project file")
        )

    return hints


def _is_test_or_temp(rel_path: str) -> bool:
    """Check if a file is likely a test or temp/debug script."""
    lower = rel_path.lower()
    parts = Path(lower).parts
    if any(p.startswith("test") for p in parts):
        return True
    if any(p in ("tests", "testing", "debug") for p in parts):
        return True
    name = Path(lower).name
    if name.startswith("debug_") or name.startswith("temp_") or name.startswith("tmp_"):
        return True
    return False


def _make_anchor(text: str) -> str:
    """Convert a heading text to a markdown anchor."""
    return text.lower().strip().replace(" ", "-").replace("/", "").replace(".", "").replace("(", "").replace(")", "")


def _toc_for_headings(headings: list[str]) -> list[str]:
    """Generate a simple markdown TOC from heading texts."""
    lines = ["## Table of Contents", ""]
    for h in headings:
        lines.append(f"- [{h}](#{_make_anchor(h)})")
    lines.append("")
    return lines


def _is_compact_eligible(analysis: AnalysisResult) -> bool:
    """True when compact profile omits optional sections."""
    return analysis.profile == "compact"


def _maybe_errors_section(analysis: AnalysisResult) -> list[str]:
    if not analysis.errors:
        return []
    lines = [
        "## Errors",
        "",
        "The following non-fatal errors were encountered during analysis:",
        "",
    ]
    for err in analysis.errors[:30]:
        lines.append(f"- {err}")
    lines.append("")
    return lines


def _render_evidence_section(analysis: AnalysisResult, profile: str) -> list[str]:
    """Render an Evidence section from the analysis evidence set."""
    ev = analysis.evidence_set
    if not ev:
        return []
    lines: list[str] = ["## Evidence", ""]
    observed = ev.by_kind(EvidenceKind.OBSERVED)
    inferred = ev.by_kind(EvidenceKind.INFERRED)
    unknowns = ev.by_kind(EvidenceKind.UNKNOWN)
    unsupported = ev.by_kind(EvidenceKind.UNSUPPORTED)
    errors = ev.by_kind(EvidenceKind.ERROR)

    if observed:
        lines.append("### Observed")
        for e in observed[: get_profile_limit(profile, "abstractions")]:
            lines.append(f"- {e.message}")
        lines.append("")
    if inferred:
        lines.append("### Inferred")
        for e in inferred[: get_profile_limit(profile, "abstractions")]:
            lines.append(f"- {e.message}")
        lines.append("")
    if unknowns:
        lines.append("### Unknown")
        for e in unknowns[: get_profile_limit(profile, "abstractions")]:
            lines.append(f"- {e.message}")
        lines.append("")
    if unsupported:
        lines.append("### Unsupported")
        for e in unsupported[: get_profile_limit(profile, "abstractions")]:
            lines.append(f"- {e.message}")
        lines.append("")
    if errors:
        lines.append("### Errors")
        for e in errors[: get_profile_limit(profile, "abstractions")]:
            lines.append(f"- {e.message}")
        lines.append("")
    return lines


def render_project_overview(
    repo_path: str,
    detect: DetectResult,
    analysis: AnalysisResult,
    output_dir: str,
    generated_at: str | None = None,
) -> RenderResult:
    """Generate PROJECT_OVERVIEW.md."""
    out = safe_output_path(Path(repo_path), output_dir)
    out.mkdir(parents=True, exist_ok=True)
    path = out / "PROJECT_OVERVIEW.md"

    timestamp = generated_at or datetime.now(timezone.utc).isoformat()

    headings = [
        "Project Metadata",
        "Root Files",
        "Source Layout",
        "Entry Points",
        "Dependencies",
    ]

    lines: list[str] = [
        GENERATED_HEADER,
    ]
    lines += _render_metadata_block(repo_path, analysis, timestamp)
    lines += [
        f"# Project Overview: {Path(repo_path).resolve().name}",
        "",
        f"- **Detected types**: {', '.join(detect.project_types)}",
        f"- **Evidence**: {', '.join(f'{k} ({v})' for k, v in detect.evidence.items())}",
        "",
    ]

    has_langs = any(
        [
            analysis.python_modules,
            analysis.node_packages,
            analysis.dotnet_projects,
            analysis.rust_crates,
            analysis.go_modules,
            analysis.java_projects,
        ]
    )
    compact_eligible = _is_compact_eligible(analysis)
    if has_langs and not (compact_eligible and len(analysis.project_types) <= 1):
        headings.insert(4, "Language Summaries")

    # Add evidence heading if there is evidence and profile is not compact on tiny repo
    if analysis.evidence_set and not compact_eligible:
        headings.insert(1, "Evidence")

    lines += _toc_for_headings(headings)

    # Metadata section
    meta = analysis.metadata
    lines += [
        "## Project Metadata",
        "",
    ]
    if meta.name:
        lines.append(f"- **Name**: {meta.name}")
    if meta.description:
        lines.append(f"- **Description**: {meta.description}")
    if meta.version:
        lines.append(f"- **Version**: {meta.version}")
    if meta.license:
        lines.append(f"- **License**: {meta.license}")
    if meta.authors:
        lines.append(f"- **Authors**: {', '.join(meta.authors)}")
    if meta.homepage:
        lines.append(f"- **Homepage**: {meta.homepage}")
    if meta.repository:
        lines.append(f"- **Repository**: {meta.repository}")
    if meta.keywords:
        lines.append(f"- **Keywords**: {', '.join(meta.keywords)}")
    if not any([meta.name, meta.description, meta.version, meta.license, meta.authors]):
        lines.append("_No project metadata found in config files._")
    lines.append("")

    if analysis.evidence_set and not compact_eligible:
        lines += _render_evidence_section(analysis, analysis.profile)

    lines += [
        "## Root Files",
        "",
        _join(analysis.root_files[: get_profile_limit(analysis.profile, "root_files")]),
        "",
        "## Source Layout",
        "",
    ]

    # Source directories for all stacks
    lines.append(_join(analysis.source_directories[: get_profile_limit(analysis.profile, "source_dirs")]))
    lines.append("")

    # Python module detail as subsection
    if analysis.python_modules:
        lines.append("### Python modules")
        lines.append("")
        source_modules = [m for m in analysis.python_modules if not _is_test_or_temp(m.rel_path)]
        pkg_tree: dict[str, list[str]] = {}
        for m in source_modules:
            parts = Path(m.rel_path).parts
            if len(parts) >= 2:
                pkg_name = parts[0]
                sub = "/".join(parts[1:])
            else:
                pkg_name = "root"
                sub = m.rel_path
            pkg_tree.setdefault(pkg_name, []).append(sub)

        limit = get_profile_limit(analysis.profile, "python_modules_display")
        # Rank package groups by importance
        ranked_pkgs = sorted(pkg_tree.items(), key=lambda item: score_path(item[0]), reverse=True)
        for pkg_name, files in ranked_pkgs:
            lines.append(f"- {_safe_inline_code(pkg_name + '/')} ({len(files)} files)")
            for f in sorted(files)[:limit]:
                lines.append(f"  - {_safe_inline_code(f)}")
            if len(files) > limit:
                lines.append(f"  - ... and {len(files) - limit} more")
            lines.append("")

    lines += [
        "## Entry Points",
        "",
        _join(analysis.likely_entry_points[: get_profile_limit(analysis.profile, "entry_points")]),
        "",
    ]

    # Dependencies section
    lines += [
        "## Dependencies",
        "",
    ]
    has_deps = False
    if analysis.python_dependencies:
        lines.append("### Python Runtime")
        for dep in analysis.python_dependencies[: get_profile_limit(analysis.profile, "deps")]:
            lines.append(f"- {dep}")
        lines.append("")
        has_deps = True
    if analysis.python_dev_dependencies:
        lines.append("### Python Development")
        for dep in analysis.python_dev_dependencies[: get_profile_limit(analysis.profile, "dev_deps")]:
            lines.append(f"- {dep}")
        lines.append("")
        has_deps = True

    # Node deps from product packages
    node_product_deps: list[str] = []
    node_dev_deps: list[str] = []
    for pkg in analysis.node_packages:
        if pkg.category == "product" and pkg.dependencies:
            node_product_deps.extend(pkg.dependencies)
        if pkg.dev_dependencies:
            node_dev_deps.extend(pkg.dev_dependencies)
    if node_product_deps:
        lines.append("### Node Runtime")
        for dep in sorted(set(node_product_deps))[: get_profile_limit(analysis.profile, "deps")]:
            lines.append(f"- {dep}")
        lines.append("")
        has_deps = True
    if node_dev_deps:
        lines.append("### Node Development")
        for dep in sorted(set(node_dev_deps))[: get_profile_limit(analysis.profile, "dev_deps")]:
            lines.append(f"- {dep}")
        lines.append("")
        has_deps = True

    if analysis.rust_crates and analysis.rust_crates[0].dependencies:
        lines.append("### Rust")
        for dep in analysis.rust_crates[0].dependencies[: get_profile_limit(analysis.profile, "deps")]:
            lines.append(f"- {dep}")
        lines.append("")
        has_deps = True
    go_deps = []
    for mod in analysis.go_modules:
        go_deps.extend(mod.dependencies)
    if go_deps:
        lines.append("### Go")
        for dep in sorted(set(go_deps))[: get_profile_limit(analysis.profile, "deps")]:
            lines.append(f"- {dep}")
        lines.append("")
        has_deps = True
    java_deps = []
    for java_proj in analysis.java_projects:
        java_deps.extend(java_proj.dependencies)
    if java_deps:
        lines.append("### Java")
        for dep in sorted(set(java_deps))[: get_profile_limit(analysis.profile, "deps")]:
            lines.append(f"- {dep}")
        lines.append("")
        has_deps = True

    if not has_deps:
        lines.append("_No dependencies identified._")
        lines.append("")

    lines += [
        "",
    ]

    if analysis.python_modules:
        lines += [
            "### Python Summary",
            "",
            f"Modules scanned: {len(analysis.python_modules)}",
            "",
        ]
        entry_mods = [m for m in analysis.python_modules if m.is_entry_point][
            : get_profile_limit(analysis.profile, "python_modules_display")
        ]
        if entry_mods:
            lines += ["#### Likely Python Entry Points", ""]
            for m in entry_mods:
                lines.append(f"- {_safe_inline_code(m.rel_path)}")
            lines.append("")
        classes: list[str] = []
        funcs: list[str] = []
        for m in analysis.python_modules:
            for c in m.classes:
                classes.append(f"{m.rel_path}::{c}")
            for f in m.functions:
                funcs.append(f"{m.rel_path}::{f}")
        lines += [f"Classes: {len(classes)}", f"Functions: {len(funcs)}", ""]

    if analysis.node_packages:
        product_pkgs = [p for p in analysis.node_packages if p.category == "product"]
        tooling_pkgs = [p for p in analysis.node_packages if p.category == "tooling"]
        other_pkgs = [p for p in analysis.node_packages if p.category not in ("product", "tooling")]
        display_pkgs = (product_pkgs + other_pkgs + tooling_pkgs)[
            : get_profile_limit(analysis.profile, "python_modules_display")
        ]
        for pkg in display_pkgs:
            cat_label = f" ({pkg.category})" if pkg.category else ""
            lines += [f"### Node/JS Summary{cat_label}", ""]
            if pkg.name:
                lines.append(f"Package: {_safe_inline_code(pkg.name)}")
            if pkg.rel_path and pkg.rel_path != "package.json":
                lines.append(f"Path: {_safe_inline_code(pkg.rel_path)}")
            if pkg.scripts:
                lines += ["#### Scripts", ""]
                for k, v in list(pkg.scripts.items())[: get_profile_limit(analysis.profile, "python_modules_display")]:
                    lines.append(f"- {_safe_inline_code(k)}: {_safe_inline_code(v)}")
                lines.append("")
            lines.append(f"JS/TS files: {len(pkg.files)}")
            if pkg.classes:
                lines.append(f"Classes: {len(pkg.classes)}")
            if pkg.functions:
                lines.append(f"Functions: {len(pkg.functions)}")
            if pkg.exports:
                lines.append(f"Exports: {len(pkg.exports)}")
            lines.append("")
        if len(analysis.node_packages) > len(display_pkgs):
            lines.append(f"_... and {len(analysis.node_packages) - len(display_pkgs)} more package(s)_")
            lines.append("")

    if analysis.dotnet_projects:
        lines += ["### .NET Summary", ""]
        for sol in analysis.dotnet_solutions:
            lines.append(f"- Solution: {_safe_inline_code(sol.name)} ({sol.rel_path})")
        for proj in analysis.dotnet_projects[: get_profile_limit(analysis.profile, "python_modules_display")]:
            lines.append(f"- Project: {_safe_inline_code(proj.name)} ({proj.rel_path})")
            if proj.target_frameworks:
                lines.append(f"  - Targets: {', '.join(proj.target_frameworks)}")
            if proj.output_type:
                lines.append(f"  - Output: {proj.output_type}")
            if proj.classes:
                lines.append(f"  - Classes: {len(proj.classes)}")
        lines.append("")

    if analysis.rust_crates:
        crate = analysis.rust_crates[0]
        lines += ["### Rust Summary", ""]
        if crate.name:
            lines.append(f"Crate: {_safe_inline_code(crate.name)}")
        lines.append(f"Modules: {len(crate.modules)}")
        if crate.traits:
            lines.append(f"Traits: {len(crate.traits)}")
        if crate.impls:
            lines.append(f"Impls: {len(crate.impls)}")
        if crate.entry_points:
            lines += ["#### Entry Points", ""]
            for ep in crate.entry_points:
                lines.append(f"- {_safe_inline_code(ep)}")
            lines.append("")
        if crate.dependencies:
            lines += ["#### Dependencies", ""]
            for dep in crate.dependencies[: get_profile_limit(analysis.profile, "deps")]:
                lines.append(f"- {dep}")
            lines.append("")

    if analysis.go_modules:
        lines += ["### Go Summary", ""]
        for mod in analysis.go_modules:
            label = f" {_safe_inline_code(mod.rel_path)}" if mod.rel_path and mod.rel_path != "." else ""
            lines.append(f"**Module{label}**")
            if mod.name:
                lines.append(f"- Name: {_safe_inline_code(mod.name)}")
            lines.append(f"- Packages: {len(mod.packages)}")
            if mod.structs:
                lines.append(f"- Structs: {len(mod.structs)}")
            if mod.interfaces:
                lines.append(f"- Interfaces: {len(mod.interfaces)}")
            if mod.entry_points:
                lines += ["  - Entry Points", ""]
                for ep in mod.entry_points:
                    lines.append(f"    - {_safe_inline_code(ep)}")
                lines.append("")
            if mod.dependencies:
                lines += ["  - Dependencies", ""]
                for dep in mod.dependencies[: get_profile_limit(analysis.profile, "deps")]:
                    lines.append(f"    - {dep}")
                lines.append("")
            lines.append("")

    if analysis.java_projects:
        lines += ["### Java Summary", ""]
        for java_proj in analysis.java_projects:
            label = (
                f" {_safe_inline_code(java_proj.rel_path)}" if java_proj.rel_path and java_proj.rel_path != "." else ""
            )
            lines.append(f"**Project{label}**")
            if java_proj.name:
                lines.append(f"- Name: {_safe_inline_code(java_proj.name)}")
            if java_proj.build_tool:
                lines.append(f"- Build tool: {_safe_inline_code(java_proj.build_tool)}")
            lines.append(f"- Classes: {len(java_proj.classes)}")
            if java_proj.methods:
                lines.append(f"- Methods: {len(java_proj.methods)}")
            if java_proj.annotations:
                lines.append(f"- Annotations: {len(java_proj.annotations)}")
            if java_proj.entry_points:
                lines += ["  - Entry Points", ""]
                for ep in java_proj.entry_points:
                    lines.append(f"    - {_safe_inline_code(ep)}")
                lines.append("")
            if java_proj.dependencies:
                lines += ["  - Dependencies", ""]
                for dep in java_proj.dependencies[: get_profile_limit(analysis.profile, "deps")]:
                    lines.append(f"    - {dep}")
                lines.append("")
            lines.append("")

    lines += _maybe_errors_section(analysis)
    if not compact_eligible:
        lines += [
            "## Generic Notes",
            "",
            f"- Test directories: {', '.join(analysis.test_directories[:10]) or 'None identified'}",
            f"- Docs files: {len(analysis.docs_files)}",
            f"- Config files: {len(analysis.config_files)}",
            "",
        ]
    lines += [
        "---",
        "*This file was generated by context-crafter-mcp and may be regenerated.*",
        "",
    ]

    write_redacted_text(path, "\n".join(lines))
    return RenderResult(
        ok=True,
        written=[str(path)],
        files_scanned=analysis.scan_summary.files_scanned,
        project_types=detect.project_types,
        resolved_output_dir=str(out),
        scan_summary=analysis.scan_summary,
    )


def render_repo_map(
    repo_path: str,
    detect: DetectResult,
    analysis: AnalysisResult,
    output_dir: str,
    generated_at: str | None = None,
) -> RenderResult:
    """Generate REPO_MAP.md."""
    out = safe_output_path(Path(repo_path), output_dir)
    out.mkdir(parents=True, exist_ok=True)
    path = out / "REPO_MAP.md"

    timestamp = generated_at or datetime.now(timezone.utc).isoformat()

    lines: list[str] = [
        GENERATED_HEADER,
    ]
    lines += _render_metadata_block(repo_path, analysis, timestamp)
    lines += [
        f"# Repository Map: {Path(repo_path).resolve().name}",
        "",
        "## Directory Tree",
        "",
        "```",
    ]
    lines += analysis.directory_tree[: get_profile_limit(analysis.profile, "tree_max_lines")]
    lines += [
        "```",
        "",
        "## Important Config Files",
        "",
        _join(analysis.config_files[: get_profile_limit(analysis.profile, "config_files")]),
        "",
        "## Likely Entry Points",
        "",
        _join(analysis.likely_entry_points[: get_profile_limit(analysis.profile, "entry_points")]),
        "",
        "## Test Directories",
        "",
        _join(analysis.test_directories[: get_profile_limit(analysis.profile, "test_dirs")]),
        "",
        "## Docs Directories",
        "",
        _join(analysis.docs_files[: get_profile_limit(analysis.profile, "docs_files")]),
        "",
        "---",
        "*Generated by context-crafter-mcp.*",
        "",
    ]

